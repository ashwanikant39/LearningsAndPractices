public class list123 {
    public static void main(String[] args) {


        int n=3;
        for (int i = 1; i <= n; i++) {
            for (int j = 1; j <= n; j++) {
                for (int k = 1; k <= n; k++) {
                    System.out.print(i);
                    System.out.print(j);

                    System.out.print(k);
                    System.out.println();
                }
               
            }
        }
    }
}
